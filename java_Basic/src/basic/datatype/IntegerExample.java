package basic.datatype;

public class IntegerExample {

	public static void main(String[] args) {
		
	 /*	## 정수형

		- byte
		    - 1byte 크기의 정수형.
		- short
		    - 2byte 크기의 정수형
		- int
		    - 4byte 크기의 정수형. -21억 정도 ~ 21억 정도
		- long
		    - 8byte 크기의 정수형. 굉장히 큰 수를 지정할 수 있다.	    

		- 자바에서 정수형 기본 데이터 타입은 int. 
		그렇기 때문에 변수에 대입되기 전 쌩 정수도 int로 인식됨. 
		대입 되기 전 값을 리터럴 이라고 함. 데이터 값 그 자체. 
		*/
		
		
		
		/*
        # 리터럴: 변수에 저장되기 전의 상수값.
        자바의 정수 리터럴의 타입은 int 입니다.
        int보다 더 큰 범위의 수를 표현하기 위해서는 뒤에 L을 붙여서
        long타입으로 인식시켜야 합니다.
        */
		
		byte a = 127; //리터럴 = 127
		short b = 32767;
		int c = 2147483647;
		long d = 2147483648L; 
		

	}

}
